from markab_vm import VMTask

def p(s):
  print(s, end='')

def test_push_pop():
  v = VMTask()
  print("=== test.vm.test_push_pop() ===")
  p(".s")
  v.dotS()
  for i in range(19):
    p(f"{i} ")
    v.push(i)
    p(".s")
    v.dotS()
  for i in range(19):
    p("drop .s")
    v.drop()
    v.dotS()
  print()

def test_plus_minus():
  v = VMTask()
  print("=== test.vm.test_plus_minus() ===")
  p("-5 1 2 .s      (  -6 1 2  OK)")
  v.push(-6)
  v.push(1)
  v.push(2)
  v.dotS()
  p("+ .s             (  -6 3  OK)")
  v.plus()
  v.dotS()
  p("+ .s               (  -3  OK)")
  v.plus()
  v.dotS()
  p("drop 7 9 .s       (  7 9  OK)")
  v.drop()
  v.push(7)
  v.push(9)
  v.dotS()
  p("- .s               (  -2  OK)")
  v.minus()
  v.dotS()
  print()

def test_multiply():
  v = VMTask()
  print("=== test.vm.test_multiply() ===")
  p("3 -1 .s                         (  3 -1  OK)")
  v.push(3)
  v.push(-1)
  v.dotS()
  p("* .s drop                         (  -3  OK)")
  v.mul()
  v.dotS()
  v.drop()
  p("-2 hex .s decimal            ( fffffffe  OK)")
  v.push(-2)
  v.dotS(hex=True)
  p("dup * .s drop                      (  4  OK)")
  v.dup()
  v.mul()
  v.dotS()
  v.drop()
  p("hex 7fffffff decimal .s   (  2147483647  OK)")
  v.push(0x7fffffff)
  v.dotS()
  p("dup * .s                           (  1  OK)")
  v.dup()
  v.mul()
  v.dotS()
  print()

def test_shift():
  v = VMTask()
  print("=== test.vm.test_shift() ===")
  p("1 31 .s                   (  1 31  OK)")
  v.push(1)
  v.push(31)
  v.dotS()
  p("<< .s              (  -2147483648  OK)")
  v.shiftLeft()
  v.dotS()
  p("hex .s decimal        (  80000000  OK)")
  v.dotS(hex=True)
  p("31 >>> decimal .s           (  -1  OK)")
  v.push(31)
  v.shiftRightI32()
  v.dotS()
  p("hex .s                (  ffffffff  OK)")
  v.dotS(hex=True)
  p("1 >> .s decimal       (  7fffffff  OK)")
  v.push(1)
  v.shiftRightU32()
  v.dotS(hex=True)
  p("decimal .s          (  2147483647  OK)")
  v.dotS()
  p("30 >> .s                      ( 1  OK)")
  v.push(30)
  v.shiftRightU32()
  v.dotS()
  p("4 << .s                     (  16  OK)")
  v.push(4)
  v.shiftLeft()
  v.dotS()
  p("2 >> .s                      (  4  OK)")
  v.push(2)
  v.shiftRightU32()
  v.dotS()
  print()



test_push_pop()
test_plus_minus()
test_multiply()
test_shift()
